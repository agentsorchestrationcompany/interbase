  async function reviewBranchOptions() {
    const lines = await gitLines([
      "for-each-ref",
      "--format=%(refname:short)",
      "refs/heads",
      "refs/remotes",
    ])
    const seen = new Set<string>()
    return lines
      .filter((branch) => !branch.endsWith("/HEAD"))
      .filter((branch) => {
        const normalized = branch.replace(/^origin\//, "")
        if (seen.has(normalized)) return false
        seen.add(normalized)
        return true
      })
      .map((branch) => ({
        label: branch,
        description: branch.startsWith("origin/") ? "Remote branch" : "Local branch",
      }))
  }

