function renderGoalFrame(title: string, detail: string, availableWidth: number): string {
  const maxFrameWidth = Math.max(4, availableWidth - 3)
  const maxInnerWidth = Math.max(1, maxFrameWidth - 4)
  const lines = [firstLine(title), singleLine(detail)]
    .filter(Boolean)
    .slice(0, 2)
    .map((line) => truncateDisplayWidth(line, maxInnerWidth))
  const innerWidth = Math.max(...lines.map((line) => Bun.stringWidth(line)), 1)
  const border = `+-${"-".repeat(innerWidth)}-+`
  return [border, ...lines.map((line) => `| ${padDisplayWidth(line, innerWidth)} |`), border].join("\n")
}

function firstLine(value: string): string {
  return value.split("\n", 1)[0]?.trimEnd() ?? ""
}

function singleLine(value: string): string {
  return value.replace(/\s+/g, " ").trim()
}

function truncateDisplayWidth(value: string, maxWidth: number): string {
  if (maxWidth <= 0) return ""
  if (Bun.stringWidth(value) <= maxWidth) return value
  if (maxWidth === 1) return "…"

  const targetWidth = maxWidth - 1
  let output = ""
  let width = 0
  for (const char of value) {
    const charWidth = Bun.stringWidth(char)
    if (width + charWidth > targetWidth) break
    output += char
    width += charWidth
  }
  return `${output}…`
}

function padDisplayWidth(value: string, width: number): string {
  return value + " ".repeat(Math.max(0, width - Bun.stringWidth(value)))
}

function modelSwitchDisplay(message: UserMessage, parts: Part[], providers: ReadonlyMap<string, Provider>) {
  const part = parts.find(
    (part) => part.type === "text" && part.synthetic && part.metadata?.kind === "interbase_model_switch",
  )
  if (part?.type !== "text") return undefined

  return {
    provider: providers.get(message.model.providerID)?.name ?? message.model.providerID,
    model: Model.name(providers, message.model.providerID, message.model.modelID),
    variant: message.model.variant ? Locale.titlecase(message.model.variant) : undefined,
  }
}

function commandResultDisplay(parts: Part[]) {
  const structured = parts.flatMap((part) => {
    if (part.type !== "text" || !part.synthetic) return []
    if (!isRecord(part.metadata) || part.metadata.kind !== "interbase_command_result") return []
    return [
      {
        title: typeof part.metadata.title === "string" ? part.metadata.title : part.text,
        detail: typeof part.metadata.detail === "string" ? part.metadata.detail : "",
      },
    ]
  })
  if (structured.length > 0) {
    const title = structured
      .map((part) => part.title)
      .filter(Boolean)
      .join("\n\n")
    return {
      title,
      detail: structured
        .map((part) => part.detail)
        .filter(Boolean)
        .join("\n\n"),
      goal: title.startsWith("Goal "),
    }
  }
  return {
    title: parts
      .flatMap((part) => (part.type === "text" && part.synthetic ? [part.text] : []))
      .filter(Boolean)
      .join("\n\n"),
    detail: "",
    goal: false,
  }
}

